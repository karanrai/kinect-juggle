#ifndef _TRIGONOMETRIC_TABLE_H_
#define _TRIGONOMETRIC_TABLE_H_

#include "common.h"

class TrigonometricTable
{
private:
	int m_slices;
	float* m_sins;
	float* m_coss;

public:
	TrigonometricTable(int slices);
	~TrigonometricTable();

	int slices() const { return m_slices; }
	float sin(int i) const { return m_sins[i % m_slices]; }
	float cos(int i) const { return m_coss[i % m_slices]; }
};

#endif
